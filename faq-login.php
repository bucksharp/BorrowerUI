			<h3>Frequently Asked Questions</h3>
			<p class="process-type">A little confused about how this works? Check out our frequently asked questions below to get a better understanding of what is going on behind the scenes.</p>

			<ol class="borrower-faq">
				<li><p><strong>What is bank account verification?</strong></p>
				<p>As a part of your application process for a loan, a line of credit, or another type of financial service, the verifier (or 
approving institution) has to make sure you own your bank accounts. The verifier also checks your account balance. 
This process is called bank account verification.</p>
				</li>
				<li><p><strong>How does online bank account verification work?</strong></p>
				<p>Your bank securely stores all of your banking information. The only way to provide access to this information online is 
through your unique security credentials. When you enter this information in the online bank account verification 
system, you allow your verifier to pull a digital copy of your bank statement from your bank.</p>
				</li>
				<li><p><strong>How many accounts do I have to verify?</strong></p>
				<p>You must verify at least one account: your primary checking account. However, if this account does not show enough
recent activity or does not have a balance that will help you earn your requested line of credit or financial service, 
your verifier may ask for additional accounts to verify. Your daily checking and savings accounts, including where you 
receive direct deposit from your employer, are the best accounts to verify.</p>
				</li>
				<li><p><strong>What if I don't see my bank once I log into online bank account verification?</strong></p>
				<p>If you don’t see your bank listed, click on “Add New Account” on the left side of the screen. Then enter either your
bank name or your routing number in one of the fields provided to search for your bank. When you do so, you will 
receive a list of possible matches. If your bank still does not appear, it is possible the bank may not be supported by 
this application. Contact your verifier and follow their instructions for providing paper bank statements.</p>
				</li>
				<li><p><strong>What if I selected the wrong bank from the list provided?</strong></p>
				<p>If you selected the wrong bank from your list of possible matches, and tried to enter credentials anyway, you will see 
an error message. To re-select or re-enter the correct bank, press the “Back” button until you see the selection screen 
again.</p>
				</li>
				<li><p><strong>What do I do if the system will not accept my bank's credentials?</strong></p>
				<p>If your credentials were not accepted by the online banking verification system, you may not have the correct bank or 
bank account type (i.e. business account versus personal account) selected. Check this by visiting the link provided for 
your bank and make sure this is where you usually log in online to access your accounts.</p>
				<p>If this is not the website you use to log in to your online banking, you will need to reselect your bank. To select the 
correct bank, click “Start Over” and begin again by selecting your bank from the list of possible matches.</p>
				<p>If this is the website you use to log in to your online banking, please verify that the credentials you are trying to use are 
correct by first logging in at your bank’s website. If you can log in on the bank’s website but not through the online 
verification system, then this particular bank account product is not supported by this application.</p>
				</li>
				<li><p><strong>What if I don’t have online banking?</strong></p>
				<p>If you do not have online banking, you can visit your bank’s website and enroll. Most banks offer an easy enrollment 
process that only takes a few minutes to complete. Once you have enrolled, you can return to the online bank account verification system and continue where you left off.</p>
				</li>
				<li><p><strong>What do I do if I was logged out before completing my verification?</strong></p>
				<p>If you were logged out, or your browser crashed before you completed your verification, and no more than three days 
have passed, you can start again by accessing the original email you received from your representative.</p>
				<p>Your original email will only be good for three days. If more than three days have passed, you will need to contact your 
verifier and ask for them to resend your invitation.</p>
				</li>
